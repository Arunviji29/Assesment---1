class centimeter {
    public static void main(String[] args) {
        double  cm , meter , kilometer;
        cm = 57;
        meter = cm / 100;
        kilometer = cm / 100000;
        System.out.println("Length in meter = " + meter + "m");
     
        System.out.println("Length in Kilometer = " + kilometer + "km");
    }
}               